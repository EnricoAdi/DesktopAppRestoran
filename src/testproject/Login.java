    private void slideShow(){
        //timer ini dijalankan setiap 4.4 detik
        //setiap intervalnya akan merubah background image
        //dari form
         timer = new Timer(4400, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                indexImageChange+=1;
                if (indexImageChange>3) {
                    indexImageChange = 0;
                }
                setImage(indexImageChange); 
            } 
        }); 
        timer.start();
    } 
    private void setImage(int idx){
        ImageIcon icon = new ImageIcon(urlImgBackground[idx]); 
        Image img = icon.getImage(); 
        Image imgScale = img.getScaledInstance(jPanel1.getWidth(), 
                jPanel1.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale); 
        labelgambar.setIcon(scaledIcon); 
    }
    private void btnHidePassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHidePassMouseClicked
          //function button ini untuk toggle hide dan unhide
          //password
        ImageIcon scaledIcon;
        if (modeHide) {
            //unhide
            txtPass.setEchoChar((char)0); 
            modeHide = false;
            
            ImageIcon icon = new ImageIcon("src\\testproject\\images\\mboxIcon\\unview.png");  
            Image imgScale = (icon.getImage()).getScaledInstance(btnHidePass.getWidth(), 
                    btnHidePass.getHeight(), Image.SCALE_SMOOTH);
            scaledIcon = new ImageIcon(imgScale); 
            btnHidePass.setIcon(scaledIcon); 
        }else {
            //hide
            txtPass.setEchoChar('•');  
            modeHide = true;
            
            ImageIcon icon = new ImageIcon("src\\testproject\\images\\mboxIcon\\view.png");  
            Image imgScale = (icon.getImage()).getScaledInstance(btnHidePass.getWidth(), 
                    btnHidePass.getHeight(), Image.SCALE_SMOOTH);
            scaledIcon = new ImageIcon(imgScale); 
            btnHidePass.setIcon(scaledIcon); 
        }
    }//GEN-LAST:event_btnHidePassMouseClicked
    
