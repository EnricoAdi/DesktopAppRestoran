    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        try {
            if (txtUser.getText().equals("admin") && txtPass.getText().equals("admin")) {
                MasterKaryawan f = new MasterKaryawan();
                f.setTitle("Ga Kenyang Ga Pulang");
                f.setResizable(false);
                f.setLocationRelativeTo(null);
                f.setVisible(true);
                timer.stop();
                this.dispose();
            }
            else{
                int jabatan = Karyawan.loginKaryawan(txtUser.getText(), txtPass.getText());
                if (jabatan == 2) {
                    FormPenghubung f = new FormPenghubung();
                    f.setTitle("Ga Kenyang Ga Pulang");
                    f.setLocationRelativeTo(null);
                    f.setVisible(true);timer.stop();
                    this.dispose();
                }
                else if (jabatan == 1) { 
                    FormPenghubungKaryawan f = new FormPenghubungKaryawan();
                    f.setTitle("Ga Kenyang Ga Pulang");
                    f.setLocationRelativeTo(null);
                    f.setVisible(true);timer.stop();
                    this.dispose();
                }
                else{ 
                    JOptionPane.showMessageDialog(this, "Error", "Gagal Login!", JOptionPane.INFORMATION_MESSAGE, 
                      new ImageIcon(logoWarning)); 
                }
            }
        } catch (Exception e) { 
            JOptionPane.showMessageDialog(this, e.getMessage(), "Gagal Login!", JOptionPane.INFORMATION_MESSAGE, 
              new ImageIcon(logoWarning)); 
        }
    }//GEN-LAST:event_jLabel3MouseClicked

