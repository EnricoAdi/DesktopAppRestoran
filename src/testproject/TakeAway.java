    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int idx = containMenu(Menu.listMenu.get(cmbMenu.getSelectedIndex()).getIdMenu());
        if (idx!=-1) {
            //bagian ini menunjukkan kalau cart sudah ada menu yang mau ditambah
            cart.get(idx).setQuantity(cart.get(idx).getQuantity()+ (int) nudQty.getValue());
        }else {
            cart.add(new DTrans(Menu.listMenu.get(cmbMenu.getSelectedIndex()).getIdMenu(), cmbMenu.getSelectedItem().toString(),
                Menu.listMenu.get(cmbMenu.getSelectedIndex()).getCategory(),
                Menu.listMenu.get(cmbMenu.getSelectedIndex()).getHarga(),
                1, (int) nudQty.getValue()));
        }
        updateJList();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (jList1.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null, "Silahkan memilih menu yang mau dihapus", 
                    "Warning", JOptionPane.INFORMATION_MESSAGE,
                new ImageIcon(Login.logoWarning));
            return;
        }
        cart.remove(jList1.getSelectedIndex());
        updateJList();
    }//GEN-LAST:event_btnDeleteActionPerformed

