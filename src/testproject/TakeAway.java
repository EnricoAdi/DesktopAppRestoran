    public void setImage(String loc){ 
        ImageIcon icon = new ImageIcon(loc);
        Image img = icon.getImage();
        Image imgScale = img.getScaledInstance(lblgambar.getWidth(), 
                lblgambar.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);
        lblgambar.setIcon(scaledIcon);  
    } 
    public void settingWarnaComboBox(){ 
        Object child = cmbMenu.getAccessibleContext().getAccessibleChild(0);
        BasicComboPopup popup = (BasicComboPopup)child;
        JList list = popup.getList();
        list.setSelectionBackground(new Color(0,239,173));
    }
    
    public int containMenu(int id){
        for (int i = 0; i < cart.size(); i++) {
            if (cart.get(i).getIdMenu() == id) {
                return i;
            }
        }
        return -1;
    }
    
    void updateJList(){
        DefaultListModel listModel = new DefaultListModel();
        for (int i = 0; i < cart.size(); i++) {
            listModel.addElement(cart.get(i).toString());
        }
        jList1.setModel(listModel);
        int temp = 0;
        for (DTrans pesan : cart) {
            temp+= pesan.getSubtotal();
        }
        lblTotal.setText("Rp. " + temp);
    }
    
    void keCheckout(Meja m){
        if (CheckoutTransaksi.tampil) { 
            JOptionPane.showMessageDialog(null, "Ada form checkout yang masih terbuka", "", JOptionPane.INFORMATION_MESSAGE, 
            new ImageIcon(Login.logoError));  
            return;
        }
        CheckoutTransaksi f = new CheckoutTransaksi(m, this);
        f.setResizable(false);
        //f.setPreferredSize(new Dimension(1000, 1000));
        this.setLocation(300, 170);
        f.setTitle("Ga Kenyang Ga Pulang");
        //f.setLocationRelativeTo(null);
        f.setLocation(1100, 170);
        f.setVisible(true);
    }
    
    public void clearCart(){
        cart.clear();
        updateJList();
        lblTotal.setText("-");
    }

